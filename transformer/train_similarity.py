def augment_data(pairs: List[Tuple[str, str, float]]) -> List[Tuple[str, str, float]]:
    """Augment training data with variations."""
    
    augmented = []
    
    for text1, text2, sim in pairs:
        augmented.append((text1, text2, sim))
        
        # Random word drops (for high similarity pairs)
        if sim > 0.8:
            words1 = text1.split()
            words2 = text2.split()
            
            if len(words1) > 5:
                # Drop 1-2 random words
                drop_idx = random.randint(1, len(words1) - 2)
                modified1 = ' '.join(words1[:drop_idx] + words1[drop_idx+1:])
                augmented.append((modified1, text2, sim * 0.95))
            
            if len(words2) > 5:
                drop_idx = random.randint(1, len(words2) - 2)
                modified2 = ' '.join(words2[:drop_idx] + words2[drop_idx+1:])
                augmented.append((text1, modified2, sim * 0.95))
        
        # Case variations
        augmented.append((text1.lower(), text2.lower(), sim))
        
    return augmented


