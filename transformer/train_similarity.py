class ContrastiveLoss(nn.Module):
    """Contrastive loss for learning similarity with harder margins."""
    
    def __init__(self, margin: float = 0.3, temperature: float = 0.1):
        super().__init__()
        self.margin = margin
        self.temperature = temperature
    
    def forward(self, embeddings1: torch.Tensor, embeddings2: torch.Tensor, 
                labels: torch.Tensor) -> torch.Tensor:
        """
        Args:
            embeddings1: [batch, dim] - embeddings of first texts
            embeddings2: [batch, dim] - embeddings of second texts
            labels: [batch] - similarity scores (0-1)
        """
        # Normalize embeddings
        embeddings1 = F.normalize(embeddings1, p=2, dim=1)
        embeddings2 = F.normalize(embeddings2, p=2, dim=1)
        
        # Cosine similarity
        cos_sim = (embeddings1 * embeddings2).sum(dim=1)  # More stable than cosine_similarity
        
        # For similar pairs (label > 0.5): pull together, target sim = label
        # For dissimilar pairs (label < 0.5): push apart, target sim = 0
        
        # Binary labels for contrastive loss
        is_similar = (labels > 0.5).float()
        
        # Pull similar pairs together
        positive_loss = is_similar * (1 - cos_sim).pow(2)
        
        # Push dissimilar pairs apart with hard margin
        negative_loss = (1 - is_similar) * F.relu(cos_sim - self.margin).pow(2)
        
        return (positive_loss + negative_loss).mean()


class CosineSimilarityLoss(nn.Module):
    """MSE loss between predicted and target similarity."""
    
    def forward(self, embeddings1: torch.Tensor, embeddings2: torch.Tensor,
                targets: torch.Tensor) -> torch.Tensor:
        """
        Args:
            embeddings1: [batch, dim]
            embeddings2: [batch, dim]  
            targets: [batch] - target similarity scores
        """
        predicted_sim = F.cosine_similarity(embeddings1, embeddings2)
        return F.mse_loss(predicted_sim, targets)


