def train_similarity_model(
    config_name: str = 'small',
    epochs: int = 20,
    batch_size: int = 32,
    learning_rate: float = 1e-4,
    device: str = None,
    save_dir: str = 'checkpoints/similarity'
):
    """
    Train the Transformer for similarity/plagiarism detection.
    """
    
    # Device
    if device is None:
        device = 'cuda' if torch.cuda.is_available() else 'cpu'
    print(f"Training on: {device}")
    
    # Create save directory
    save_path = Path(__file__).parent / save_dir
    save_path.mkdir(parents=True, exist_ok=True)
    
    # === CREATE DATA ===
    print("\n=== Creating Training Data ===")
    paraphrase_pairs = create_paraphrase_data()
    exam_pairs = create_exam_answer_data()
    
    all_pairs = paraphrase_pairs + exam_pairs
    all_pairs = augment_data(all_pairs)
    
    print(f"Total training pairs: {len(all_pairs)}")
    
    # Split train/val
    random.shuffle(all_pairs)
    split_idx = int(len(all_pairs) * 0.9)
    train_pairs = all_pairs[:split_idx]
    val_pairs = all_pairs[split_idx:]
    
    print(f"Training pairs: {len(train_pairs)}")
    print(f"Validation pairs: {len(val_pairs)}")
    
    # === BUILD VOCABULARY ===
    print("\n=== Building Vocabulary ===")
    all_texts = []
    for t1, t2, _ in all_pairs:
        all_texts.extend([t1, t2])
    
    tokenizer = SimpleTokenizer(vocab_size=10000, min_freq=1)
    tokenizer.build_vocab(all_texts)
    print(f"Vocabulary size: {len(tokenizer)}")
    
    # Save tokenizer
    tokenizer.save(str(save_path / 'tokenizer.json'))
    
    # === CREATE DATASETS ===
    train_dataset = SimilarityDataset(train_pairs, tokenizer, max_length=128)
    val_dataset = SimilarityDataset(val_pairs, tokenizer, max_length=128)
    
    train_loader = DataLoader(train_dataset, batch_size=batch_size, shuffle=True)
    val_loader = DataLoader(val_dataset, batch_size=batch_size)
    
    # === CREATE MODEL ===
    print("\n=== Creating Model ===")
    
    vocab_size = len(tokenizer)
    
    if config_name == 'tiny':
        # Tiny config
        d_model = 128
        n_heads = 4
        n_layers = 2
        d_ff = 256
    elif config_name == 'small':
        # Small config
        d_model = 256
        n_heads = 4
        n_layers = 4
        d_ff = 512
    else:
        # Base config
        d_model = 512
        n_heads = 8
        n_layers = 6
        d_ff = 2048
    
    transformer = Transformer(
        src_vocab_size=vocab_size,
        tgt_vocab_size=vocab_size,
        d_model=d_model,
        n_heads=n_heads,
        n_encoder_layers=n_layers,
        n_decoder_layers=n_layers,
        d_ff=d_ff,
        max_seq_len=128,
        dropout=0.1,
        pad_token=tokenizer.pad_token_id
    )
    model = SimilarityEncoder(transformer, pooling='mean', d_model=d_model)
    model = model.to(device)
    
    num_params = sum(p.numel() for p in model.parameters())
    print(f"Model parameters: {num_params:,}")
    
    # === TRAINING SETUP ===
    optimizer = torch.optim.AdamW(model.parameters(), lr=learning_rate, weight_decay=0.01)
    
    total_steps = len(train_loader) * epochs
    scheduler = CosineAnnealingWarmup(
        optimizer,
        warmup_steps=min(500, total_steps // 10),
        total_steps=total_steps
    )
    
    # Use combination of losses
    contrastive_loss_fn = ContrastiveLoss(margin=0.5)
    mse_loss_fn = CosineSimilarityLoss()
    
    # === TRAINING LOOP ===
    print("\n=== Starting Training ===")
    print(f"Epochs: {epochs}")
    print(f"Batch size: {batch_size}")
    print(f"Learning rate: {learning_rate}")
    print("=" * 50)
    
    best_val_loss = float('inf')
    train_losses = []
    val_losses = []
    
    for epoch in range(epochs):
        # Training
        model.train()
        epoch_loss = 0
        
        for batch in train_loader:
            text1 = batch['text1'].to(device)
            text2 = batch['text2'].to(device)
            similarity = batch['similarity'].to(device)
            
            optimizer.zero_grad()
            
            # Forward
            emb1, emb2 = model(text1, text2)
            
            # Combined loss
            loss_contrastive = contrastive_loss_fn(emb1, emb2, similarity)
            loss_mse = mse_loss_fn(emb1, emb2, similarity)
            loss = 0.5 * loss_contrastive + 0.5 * loss_mse
            
            # Backward
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            scheduler.step()
            
            epoch_loss += loss.item()
        
        avg_train_loss = epoch_loss / len(train_loader)
        train_losses.append(avg_train_loss)
        
        # Validation
        model.eval()
        val_loss = 0
        correct_predictions = 0
        total_predictions = 0
        
        with torch.no_grad():
            for batch in val_loader:
                text1 = batch['text1'].to(device)
                text2 = batch['text2'].to(device)
                similarity = batch['similarity'].to(device)
                
                emb1, emb2 = model(text1, text2)
                
                loss_contrastive = contrastive_loss_fn(emb1, emb2, similarity)
                loss_mse = mse_loss_fn(emb1, emb2, similarity)
                loss = 0.5 * loss_contrastive + 0.5 * loss_mse
                
                val_loss += loss.item()
                
                # Check accuracy (predicted similarity within 0.2 of target)
                predicted_sim = F.cosine_similarity(emb1, emb2)
                correct = (torch.abs(predicted_sim - similarity) < 0.2).sum().item()
                correct_predictions += correct
                total_predictions += similarity.size(0)
        
        avg_val_loss = val_loss / len(val_loader)
        val_losses.append(avg_val_loss)
        accuracy = correct_predictions / total_predictions * 100
        
        print(f"Epoch {epoch+1}/{epochs} | "
              f"Train Loss: {avg_train_loss:.4f} | "
              f"Val Loss: {avg_val_loss:.4f} | "
              f"Accuracy: {accuracy:.1f}%")
        
        # Save best model
        if avg_val_loss < best_val_loss:
            best_val_loss = avg_val_loss
            torch.save({
                'epoch': epoch,
                'model_state_dict': model.state_dict(),
                'transformer_state_dict': model.transformer.state_dict(),
                'optimizer_state_dict': optimizer.state_dict(),
                'config': {
                    'vocab_size': vocab_size,
                    'd_model': d_model,
                    'n_heads': n_heads,
                    'n_layers': n_layers,
                    'd_ff': d_ff,
                    'config_name': config_name
                },
                'train_loss': avg_train_loss,
                'val_loss': avg_val_loss,
            }, save_path / 'best_model.pt')
            print(f"  ↳ Saved best model!")
    
    # Save final model
    torch.save({
        'epoch': epochs,
        'model_state_dict': model.state_dict(),
        'transformer_state_dict': model.transformer.state_dict(),
        'config': {
            'vocab_size': vocab_size,
            'd_model': d_model,
            'n_heads': n_heads,
            'n_layers': n_layers,
            'd_ff': d_ff,
            'config_name': config_name
        },
        'train_losses': train_losses,
        'val_losses': val_losses,
    }, save_path / 'final_model.pt')
    
    print("\n" + "=" * 50)
    print("Training Complete!")
    print(f"Best validation loss: {best_val_loss:.4f}")
    print(f"Models saved to: {save_path}")
    
    return model, tokenizer


